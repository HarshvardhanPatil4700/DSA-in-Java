public class OutputInJava {
    public static void main(String[] args) {
        // 1. System.out.print()
        System.out.print("Hello World"); 
        System.out.print("Welcome to JAVA"); 
        System.out.print("1 2 3"); // this prints all the statements in a single line. To get o/p on next line use System.out.println() OR use new line character at end of each string for System.out.print(): 

        // 2. System.out.print() by using \n
        System.out.println("\n");  // new line 
        System.out.print("Hello World\nHi! ");  
        System.out.print("welcome to java\n");   
        System.out.print("1 2 3\n");   
        
        // 3. System.out.println() OR sysout
        System.out.println("Hello World"); 
        System.out.println("Welcome to JAVA"); 
        System.out.println("1 2 3"); 
    }
}